"use client";

import { useDrag } from 'react-dnd';

// Component for a draggable component in the library
function DraggableComponent({ type, name, icon }) {
  const [{ isDragging }, drag] = useDrag(() => ({
    type: 'COMPONENT',
    item: { type },
    collect: (monitor) => ({
      isDragging: !!monitor.isDragging(),
    }),
  }));
  
  return (
    <div
      ref={drag}
      className={`flex items-center p-3 mb-2 border border-gray-200 dark:border-gray-700 rounded-md cursor-move bg-white dark:bg-gray-800 hover:bg-gray-50 dark:hover:bg-gray-700 transition-colors ${
        isDragging ? 'opacity-50' : 'opacity-100'
      }`}
    >
      <div className="mr-3 text-gray-500 dark:text-gray-400">{icon}</div>
      <span className="text-gray-700 dark:text-gray-300">{name}</span>
    </div>
  );
}

